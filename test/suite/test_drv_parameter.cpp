static int isp_parameter_name_to_map_index(char *key)
{
  int i = 0;
  while (i < sizeof(ISP_Param_Map) / sizeof(PARAM_TAG)) {
	  if (0 == strcmp(ISP_Param_Map[i].Name, key)) {
		  return i;
	  } else {
		i++;
	  }
  }
  return -1;
}

static char *trim(char *src)
{
	int i = 0;
    char *begin = src;
    while (src[i] != '\0') {
		if (src[i] != ' ' && src[i] != '\t' && src[i] != '\n'&& src[i] != '\r') {
			break;
		} else {
			begin++;
		}
		i++;
	}
    for (i = strlen(src)-1; i >= 0; i--) {
		if (src[i] != ' ' && src[i] != '\t' && src[i] != '\n'&& src[i] != '\r'){
			break;
        } else {
			src[i] = '\0';
        }
   }
   return begin;
}

